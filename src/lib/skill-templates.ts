const OPTIMIZE_SURVIVAL = `"""
scenario/optimize_survival.py — cash-crunch survival planner

Uses PuLP for mixed-integer optimization. Decision variables:
  - which overdue invoices to prioritize for collection
  - which vendor payments to delay
  - which non-essential subscriptions to pause
Objective: maximize Day-18 buffer subject to keeping balance > floor.
"""
from pulp import LpProblem, LpMaximize, LpVariable, lpSum, LpBinary

def solve(invoices, vendors, subs, daily_balance, day_target=18):
    prob = LpProblem("survival", LpMaximize)
    x = {i["id"]: LpVariable(f"collect_{i['id']}", cat=LpBinary) for i in invoices}
    y = {v["id"]: LpVariable(f"delay_{v['id']}",   cat=LpBinary) for v in vendors}
    z = {s["id"]: LpVariable(f"pause_{s['id']}",   cat=LpBinary) for s in subs if not s["essential"]}

    inflow  = lpSum(x[i["id"]] * i["amount"] * 0.85       for i in invoices)
    delayed = lpSum(y[v["id"]] * v["amount"]              for v in vendors)
    paused  = lpSum(z[s["id"]] * s["monthly_cost"]        for s in subs if not s["essential"])

    prob += inflow + delayed + paused
    prob += daily_balance[day_target] + inflow + delayed + paused >= 5_000

    prob.solve()
    return {
        "collect":     [i["id"] for i in invoices if x[i["id"]].value() == 1],
        "delay":       [v["id"] for v in vendors  if y[v["id"]].value() == 1],
        "pause":       [s["id"] for s in subs     if not s["essential"] and z[s["id"]].value() == 1],
        "buffer_gain": int(prob.objective.value()),
    }
`;


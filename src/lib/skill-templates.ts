const CASH_FORECAST = `"""
treasury/cash_forecast.py — 30-day cash projection
Generated by Codex Prime for Treasury Sentinel.
"""
import pandas as pd

SAFETY_FLOOR = 5_000
OPENING_BALANCE = 32_400

inflows = [
    {"day": 5,  "amount":  4_200, "label": "Hexa Studios (INV-1025)"},
    {"day": 18, "amount":  4_400, "label": "Mango Retail (INV-1011)"},
    {"day": 28, "amount":  8_600, "label": "Recurring MRR (Stripe)"},
]
outflows = [
    {"day": 12, "amount": 6_200,  "label": "Vendor: Acme Cloud Hosting"},
    {"day": 14, "amount": 1_800,  "label": "Vendor: Twilio"},
    {"day": 15, "amount": 22_000, "label": "Payroll"},
    {"day": 22, "amount": 3_170,  "label": "AWS Reserved"},
]

def project(days: int = 30) -> pd.DataFrame:
    bal = OPENING_BALANCE
    rows = []
    for d in range(days + 1):
        delta = sum(i["amount"] for i in inflows if i["day"] == d) \\
              - sum(o["amount"] for o in outflows if o["day"] == d)
        bal += delta
        rows.append({"day": d, "balance": bal, "below_floor": bal < SAFETY_FLOOR})
    return pd.DataFrame(rows)

if __name__ == "__main__":
    df = project()
    breach = df[df["below_floor"]].head(1)
    if not breach.empty:
        d = int(breach.iloc[0]["day"])
        b = int(breach.iloc[0]["balance"])
        print(f"CRUNCH on Day {d}: balance {b:,} USD")
    print(df.tail(10).to_string(index=False))
`;

const SEGMENT_CUSTOMERS = `"""
collection/segment_customers.py — payment-behavior classifier
"""
import pandas as pd

def classify(invoices: pd.DataFrame, history: pd.DataFrame) -> pd.DataFrame:
    rates = history.groupby("customer")["paid_late"].mean()
    invoices["chronic"] = invoices["customer"].map(rates) > 0.4
    invoices["tone"] = invoices.apply(
        lambda r: "firm" if r["chronic"] or r["days_late"] >= 14 else "friendly",
        axis=1,
    )
    invoices["priority"] = invoices["amount"] / (invoices["days_late"].clip(lower=1))
    return invoices.sort_values("priority", ascending=False)
`;

